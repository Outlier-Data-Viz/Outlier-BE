DROP TABLE IF EXISTS auth CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS states CASCADE;
DROP TABLE IF EXISTS populations CASCADE;
DROP TABLE IF EXISTS topics CASCADE;
DROP TABLE IF EXISTS additional_data CASCADE;
DROP TABLE IF EXISTS favorites CASCADE;
DROP TABLE IF EXISTS resources; 

CREATE TABLE auth (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  email TEXT NOT NULL UNIQUE,
  password_hash TEXT NOT NULL
);

CREATE TABLE users (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  auth_email TEXT NOT NULL UNIQUE,
  username TEXT,
  avatar TEXT,
  FOREIGN KEY (auth_email) REFERENCES auth(email)
);

CREATE TABLE topics (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE
);


CREATE TABLE states (
  state_name TEXT NOT NULL PRIMARY KEY,
  abrv TEXT UNIQUE NOT NULL
);

CREATE TABLE resources (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  resource_name TEXT NOT NULL,
  resource_url TEXT NOT NULL,
  state_abrv TEXT,
  topics_id BIGINT,
  FOREIGN KEY (state_abrv) REFERENCES states(abrv),
  FOREIGN KEY (topics_id) REFERENCES topics(id)
);

CREATE TABLE favorites (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  image TEXT NOT NULL,
  user_id BIGINT NOT NULL REFERENCES users(id),
  topic_id BIGINT NOT NULL REFERENCES topics(id)
);

CREATE TABLE additional_data (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  data DECIMAL(8,2),
  state TEXT NOT NULL REFERENCES states(abrv),
  topic TEXT NOT NULL REFERENCES topics(name)
);

CREATE TABLE populations (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  state_abrv TEXT,
  total NUMERIC,
  lgbt NUMERIC,
  aa Numeric,
  latinx Numeric,
  -- api DECIMAL(8,2),
  FOREIGN KEY (state_abrv) REFERENCES states(abrv)
);

